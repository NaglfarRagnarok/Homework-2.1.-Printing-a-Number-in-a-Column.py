# Запитуємо число
number = int(input("Введіть 5-ти значне число: "))

# Виділяємо цифри
d1 = number // 10000             # десятки тисяч
d2 = (number // 1000) % 10       # тисячі
d3 = (number // 100) % 10        # сотні
d4 = (number // 10) % 10         # десятки
d5 = number % 10                 # одиниці

# Перевертаємо число
reversed_number = d5 * 10000 + d4 * 1000 + d3 * 100 + d2 * 10 + d1

# Виводимо результат
print("Перевернуте число:", reversed_number)
