# Запитуємо число
number = int(input("Введіть 4-х значне число:1234"))

# Виділяємо цифри
d1 = number // 1000                # тисячі
d2 = (number // 100) % 10          # сотні
d3 = (number // 10) % 10           # десятки
d4 = number % 10                   # одиниці

# Виводимо цифри у стовпчик
print(d1)
print(d2)
print(d3)
print(d4)
